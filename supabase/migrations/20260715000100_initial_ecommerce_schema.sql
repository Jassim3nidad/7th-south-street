create index products_featured_idx on public.products(is_featured) where is_featured;

create table public.product_variants (
  id bigint primary key generated by default as identity,
  product_id bigint not null references public.products(id) on delete cascade,
  sku text not null unique,
  size text not null default 'OS',
  color text not null default '',
  price numeric(12, 2) not null,
  compare_at_price numeric(12, 2),
  stock_quantity integer not null default 0,
  low_stock_threshold integer not null default 5,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (product_id, size, color),
  constraint variants_sku_not_blank check (btrim(sku) <> ''),
  constraint variants_size_not_blank check (btrim(size) <> ''),
  constraint variants_price_nonnegative check (price >= 0),
  constraint variants_compare_price_valid check (compare_at_price is null or compare_at_price >= price),
  constraint variants_stock_nonnegative check (stock_quantity >= 0),
  constraint variants_threshold_nonnegative check (low_stock_threshold >= 0)
);

create index product_variants_product_id_idx on public.product_variants(product_id);
create index product_variants_active_idx on public.product_variants(product_id, is_active);

