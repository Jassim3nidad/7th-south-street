create table public.orders (
  id bigint primary key generated by default as identity,
  order_number text not null unique,
  idempotency_key uuid not null unique,
  customer_id bigint references public.customers(id) on delete set null,
  customer_user_id uuid references auth.users(id) on delete set null,
  guest_email text,
  status public.order_status not null default 'pending',
  payment_method text not null default 'cod',
  payment_status public.payment_status not null default 'unpaid',
  payment_reference text,
  shipping_name text not null,
  shipping_email text not null,
  shipping_phone text not null,
  shipping_address text not null,
  shipping_city text not null,
  shipping_province text,
  shipping_postal text,
  shipping_country text not null default 'Philippines',
  subtotal numeric(12, 2) not null,
  shipping_fee numeric(12, 2) not null default 0,
  discount_amount numeric(12, 2) not null default 0,
  total numeric(12, 2) not null,
  notes text,
  shipped_at timestamptz,
  delivered_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint orders_number_format check (order_number ~ '^7SS-[A-F0-9]{8}$'),
  constraint orders_email_format check (shipping_email ~* '^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$'),
  constraint orders_payment_method check (payment_method in ('cod', 'gcash', 'bank')),
  constraint orders_amounts_nonnegative check (
    subtotal >= 0 and shipping_fee >= 0 and discount_amount >= 0 and total >= 0
  ),
  constraint orders_total_consistent check (total = subtotal + shipping_fee - discount_amount)
);

create index orders_customer_user_id_idx on public.orders(customer_user_id);
create index orders_status_idx on public.orders(status);
create index orders_created_at_idx on public.orders(created_at desc);

