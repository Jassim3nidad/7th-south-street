create or replace function private.set_updated_at()
returns trigger
language plpgsql
set search_path = pg_catalog
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'profiles', 'categories', 'products', 'product_variants', 'customers',
    'customer_addresses', 'orders', 'events'
  ] loop
    execute format(
      'create trigger set_%1$s_updated_at before update on public.%1$I for each row execute function private.set_updated_at()',
      table_name
    );
  end loop;
end;
$$;

